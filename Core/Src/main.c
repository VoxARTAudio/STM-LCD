int dsp(int lSample, int rSample) {
	// Mono Sum
	float sum = (float)(lSample + rSample);
	
	int pitchedSumSample = Do_PitchShift(sum);
	
	float reverbSumSample = wet*Do_Reverb(sum); //Wet Mix
	
	float chorusSample = ApplyChorusWithReverbAndPitchShift(sum);
	
	//return (reverbSumSample + pitchedSumSample)*0.5;
	return chorusSample;
}

void HAL_I2SEx_TxRxHalfCpltCallback(I2S_HandleTypeDef *hi2s) {
	//restore signed 24 bit sample from 16-bit buffers
	int lSample = (int) (rxBuf[0]<<16)|rxBuf[1];
	int rSample = (int) (rxBuf[2]<<16)|rxBuf[3];
	
	int effects = dsp(lSample, rSample);
	
	//Set L/R Channels to Mono Values
	lSample = (int) effects;
	rSample = lSample;
	
	//restore to buffer
	txBuf[0] = (lSample>>16)&0xFFFF;
	txBuf[1] = lSample&0xFFFF;
	txBuf[2] = (rSample>>16)&0xFFFF;
	txBuf[3] = rSample&0xFFFF;
}

void HAL_I2SEx_TxRxCpltCallback(I2S_HandleTypeDef *hi2s) {
	//restore signed 24 bit sample from 16-bit buffers
	int lSample = (int) (rxBuf[4]<<16)|rxBuf[5];
	int rSample = (int) (rxBuf[6]<<16)|rxBuf[7];
	
	int effects = dsp(lSample, rSample);
	
	//Set L/R Channels to Mono Values
	lSample = (int) effects;
	rSample = lSample;

	//restore to buffer
	txBuf[4] = (lSample>>16)&0xFFFF;
	txBuf[5] = lSample&0xFFFF;
	txBuf[6] = (rSample>>16)&0xFFFF;
	txBuf[7] = rSample&0xFFFF;
}

